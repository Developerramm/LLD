#include <iostream>
using namespace std;

int main(){

    cout << "Operator in cpp " << endl;
    int a = 5;
    cout << ++a << endl;

    cout << a++ << endl;
    cout << a << endl;

    int b = 10;
    cout << b-- << endl;
    cout << b << endl;
    cout << --b << endl;
    cout << b << endl;

    return 0;
}